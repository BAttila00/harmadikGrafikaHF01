struct Camera { // 3D camera
//---------------------------
	// extrinsic
	vec3 wEye;			//kamera posíció
	vec3 wLookat;		//nézeti irány
	vec3 wVup;			//preferált függöleges irány
	// intrinsic
	float fov;			//függöleges látószög
	float asp;			//aspektus arány (a függöleges/a vízszintes látószög aránya) (lásd 9. diasor 17. és 26. dia)
	float fp;			//elsö vágósík távolsága (lásd 9. diasor 17. és 26. dia)
	float bp;			//hátsó vágósík távolsága (lásd 9. diasor 17. és 26. dia)
public:
	Camera() {
		asp = (float)windowWidth / windowHeight;
		fov = 75.0f * (float)M_PI / 180.0f;
		fp = 1; bp = 20;
	}
	/// <summary>
	/// Azért transzformáltunk, hogy a láthatósági feladatot és a vetítést képernyö koordinátarendszerben oldhassuk meg
	/// </summary>
	/// <returns></returns>
	mat4 V() { // view matrix: translates the center to the origin
		vec3 w = normalize(wEye - wLookat);
		vec3 u = normalize(cross(wVup, w));
		vec3 v = cross(w, u);
		return TranslateMatrix(wEye * (-1)) * mat4(u.x, v.x, w.x, 0,
			u.y, v.y, w.y, 0,
			u.z, v.z, w.z, 0,
			0, 0, 0, 1);
	}

	/// <summary>
	/// Azért transzformáltunk, hogy a láthatósági feladatot és a vetítést képernyö koordinátarendszerben oldhassuk meg
	/// </summary>
	/// <returns></returns>
	mat4 P() { // projection matrix
		return mat4(1 / (tan(fov / 2) * asp), 0, 0, 0,
			0, 1 / tan(fov / 2), 0, 0,
			0, 0, -(fp + bp) / (bp - fp), -1,
			0, 0, -2 * fp * bp / (bp - fp), 0);
	}
};

