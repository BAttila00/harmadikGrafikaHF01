struct Material {	//inkrementális képszintézisnél diffúz felületekkel dolgozunk többnyire
	//---------------------------

	vec3 kd;	//diffúz visszaverödési tényezö
	vec3 ks;	//spekuláris visszaverödési tényezö
	vec3 ka;	//ambiens visszaverödési tényezö
	float shininess;
};

//---------------------------
struct Light {
	//---------------------------
	vec3 La;	//ambiens fényforrás (itt most minden fényforráshoz felvett külön ambiens fényt, lehetne úgy is h csak egy ambiens fény van globálisan)
	vec3 Le;	//az adott fényforrás sugársürüsége, intenzitása, fényerössége stb stb
	vec4 wLightPos; //fényforrás helye		// homogeneous coordinates, can be at ideal point	
	//mivel ez egy homogén koordináta, így lehet az ideális pontban (végtelenben) (ha az utsó koordináta nulla)
	//mivel ha egy pontszerü fényforrás a végtelenben van akkor azt irányfényforrásnak vesszük ezért ezzel a struktúrával a pont és irány fényforrások is kezelhetöek
};

